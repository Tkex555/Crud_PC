package mundo;

public class Marca {
	
	private int id;
	private String nombre;
	private String descripcion;
	private String estado;
	
	
	public Marca(int id, String nombre, String descripcion, String estado) {
		this.id = id;
		this.nombre = nombre;
		this.descripcion = descripcion;
		this.estado = estado;
	}

}
